    <h2>tentang kami</h2>

    <p> <b>.Feast</b> adalah salah satu nama besar yang telah berhasil memberikan warna unik dalam industri musik alternatif di Indonesia. 
        Dikenal dengan kandungan lirik yang penuh dengan pesan sosial dan gaya musik yang eksperimental, 
        Feast berhasil mendapat perhatian besar dari pernikmat musik lokal. 
        Dalam artikel ini, akan membahas lebih lanjut mengenai sejarah, inspirasi dan perjalanan karier Feast.
     </p>

     <h4>awal terbentuk</h4>
     <p>Band .Feast dibentuk pada tahun 2012 oleh sekumpulan mahasiswa Universitas Indonesia. Awalnya, 
        Feast hanya sebuah projek musik di kampus, tetapi karena keunikan dalam menyampaikan pesan melalui 
        lirik dan aransemen musik yang beragam maka hal itu menarik banyak perhatian.
     </p>

     <h4>
        asal nama 

     </h4>

     <p>".Feast" dipilih karena sebuah makna yang menggambarkan "pesan" atau "perayaan". Sehingga dalam hal ini, 
        para anggota ingin mengundang para pendengar untuk terlibat dalam perayaan musik yang penuh pesan dan energi. </p>
    
